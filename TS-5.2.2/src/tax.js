/**
 * Calculate Tax Function
 * @param {number} income - Number parameter income
 * @returns number
 */

export function calcTax(income) {
  return income * 1.5
}

/**
 * Greet Function
 * @param {string} name - Give your name as parameter
 * @returns string
 */

export function greet(name) {
  return "Hello " + name
}
